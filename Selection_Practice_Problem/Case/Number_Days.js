let Day = "";
switch (Math.floor(Math.random() * 7)) {
  case 1:
    Day = "Sunday";
    break;
  case 2:
    Day = "Monaday";
    break;
  case 3:
    Day = "Tuesday";
    break;
  case 4:
    Day = "Wednesday";
    break;
  case 5:
    Day = "Thursday";
    break;
  case 6:
    Day = "Friday";
    break;
  case 7:
    Day = "Saturday";
    break;
}
console.log(Day);
