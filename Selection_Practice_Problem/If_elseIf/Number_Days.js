//Read the Numbers and Display the Days

let Day = Math.floor(Math.random() * 7) + 1;
if (Day == 1) {
  console.log("Sunday");
} else if (Day == 2) {
  console.log("Monday");
} else if (Day == 3) {
  console.log("Tuesday");
} else if (Day == 4) {
  console.log("Wednesday");
} else if (Day == 5) {
  console.log("Thrusday");
} else if (Day == 6) {
  console.log("Friday");
} else {
  console.log("Saturday");
}
